#include "lex.h"

int			is_letter(char c)
{
	int		f;

	f = 1;
	if (c == '>' || c == '<' || c == '|' ||\
	c == ' ' || c == '\t' || c == ';' ||\
	c == '\'' || c == 0 || c == '\"' || is_ws(c))
		f = 0;
	return (f);
}

int			is_digit(char c)
{
	int		f;

	f = 0;
	if (c >= '0' && c <= '9')
		f = 1;
	return (f);
}

int			is_ws(char c)
{
	int		f;

	f = 0;
	if (c == ' ' || c == '\t')
		f = 1;
	return (f);
}
